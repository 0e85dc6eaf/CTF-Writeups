def reverseChar(res):
	bin_r = bin(res)[2:]
	bin_r = "0"*(8-len(bin_r))+bin_r
	x = bytearray(bin_r.encode('utf-8'))
	swaps = [(6,7),(2,5),(3,4),(0,1),(4,7),(5,6),(0,3),(1,2)]
	for i,j in swaps:
		x[i],x[j]=x[j],x[i]
	return chr(int(x.decode('utf-8'),2))
print("picoCTF{%s}"%"".join([reverseChar(x) for x in expected]))